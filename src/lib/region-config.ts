/**
 * 获取区域的排序编码（用于按行政区划代码排序）
 */
export function getRegionSortCode(name: string): string {
  const prov = PROVINCES.find(p => p.shortName === name);
  if (prov) return prov.code;
  if (CITY_CODES[name]) return CITY_CODES[name];
  if (COUNTY_TO_PROVINCE[name]) return COUNTY_TO_PROVINCE[name] + '9' + name;
  return '999999';
}

/**
 * 将任意区域值规范化为标准简称，并返回所属省份代码
 * @returns { normalizedRegion, provinceCode } 或 null（无法匹配）
 */
export function normalizeRegion(raw: string): { normalizedRegion: string; provinceCode: string } | null {
  if (!raw || raw === '全国') return { normalizedRegion: '全国', provinceCode: '000000' };

  const trimmed = raw.trim();

  // 1. 直接匹配省份简称
  const province = PROVINCES.find(p => p.shortName === trimmed);
  if (province) return { normalizedRegion: province.shortName, provinceCode: province.code };

  // 2. 直接匹配城市简称
  if (CITY_TO_PROVINCE[trimmed]) {
    return { normalizedRegion: trimmed, provinceCode: CITY_TO_PROVINCE[trimmed] };
  }

  // 3. 直接匹配自治县
  if (COUNTY_TO_PROVINCE[trimmed]) {
    return { normalizedRegion: trimmed, provinceCode: COUNTY_TO_PROVINCE[trimmed] };
  }

  // 4. 修复"X市城"模式（如"湘潭市城" → "湘潭"）
  if (trimmed.endsWith('市城')) {
    const cityName = trimmed.slice(0, -2);
    if (CITY_TO_PROVINCE[cityName]) {
      return { normalizedRegion: cityName, provinceCode: CITY_TO_PROVINCE[cityName] };
    }
  }

  // 5. 修复"X城"模式（如"杭州城" → "杭州"）
  if (trimmed.endsWith('城') && !trimmed.endsWith('市城')) {
    const cityName = trimmed.slice(0, -1);
    if (CITY_TO_PROVINCE[cityName]) {
      return { normalizedRegion: cityName, provinceCode: CITY_TO_PROVINCE[cityName] };
    }
  }

  // 6. 匹配"X市"格式（如"宁波市" → "宁波"）
  if (trimmed.endsWith('市')) {
    const cityName = trimmed.slice(0, -1);
    if (CITY_TO_PROVINCE[cityName]) {
      return { normalizedRegion: cityName, provinceCode: CITY_TO_PROVINCE[cityName] };
    }
  }

  // 7. 尝试匹配"X市菜"等垃圾数据（如"宁波市菜" → "宁波"）
  for (const [city, code] of Object.entries(CITY_TO_PROVINCE)) {
    if (trimmed.startsWith(city)) {
      return { normalizedRegion: city, provinceCode: code };
    }
  }

  // 8. 尝试匹配省份全称
  const provFull = PROVINCES.find(p => trimmed.startsWith(p.shortName));
  if (provFull) return { normalizedRegion: provFull.shortName, provinceCode: provFull.code };

  return null;
}

/**
 * 获取省份简称（从省份代码）
 */
export function getProvinceShortName(code: string): string {
  if (code === '000000') return '全国';
  const p = PROVINCES.find(pr => pr.code === code);
  return p?.shortName || '未知';
}

/**
 * 获取所有省份（按行政编码排序），用于侧边栏展示
 */
export function getProvincesForDisplay(): Array<{ code: string; shortName: string }> {
  return [
    { code: '000000', shortName: '全国' },
    ...PROVINCES.map(p => ({ code: p.code, shortName: p.shortName })),
  ];
}

// === 前台展示的省份白名单 ===
// 只展示这些省份（及其下属城市/自治县）的法规，全国性法规始终展示
export const ALLOWED_PROVINCES = ['湖南', '海南', '山东', '江苏', '浙江'];

/**
 * 获取白名单内所有合法的 region 值（省份 + 城市 + 自治县）
 */
export function getAllowedRegionValues(): string[] {
  const allowed = new Set<string>();
  allowed.add('全国');

  const allowedCodes = new Set<string>();
  for (const prov of PROVINCES) {
    if (ALLOWED_PROVINCES.includes(prov.shortName)) {
      allowed.add(prov.shortName);
      allowedCodes.add(prov.code);
    }
  }

  for (const [city, code] of Object.entries(CITY_TO_PROVINCE)) {
    if (allowedCodes.has(code)) allowed.add(city);
  }

  for (const [county, code] of Object.entries(COUNTY_TO_PROVINCE)) {
    if (allowedCodes.has(code)) allowed.add(county);
  }

  return Array.from(allowed);
}
